public class Mate {
    public static void main(String[] args) {
        // raiz
        double raiz = Math.sqrt(49);
        System.out.println("La raiz es: " + raiz);

        // potencia
        double base = 5;
        double exponente = 3;
        double potencia = Math.pow(base, exponente);
        System.out.println("La potencia es: " + potencia);

        //seno
        double seno = Math.sin(90);
        System.out.println("El seno es: " + seno);

        //coseno
        double coseno = Math.cos(45);
        System.out.println("El coseno es: " + coseno);

        //tangente
        double tangente = Math.round(Math.tan(180));
        System.out.println("La tangente es: " + tangente);
    }
}
