public class Switch {
    public static void main(String[] args) {
        int valor = 50;
        switch (valor) {
        case 100:
            System.out.println("El valor es mayor que 50");
            break;
        case 50:
            System.out.println("El valor es igual a 50");
            break;
        default:
            break;
        }
    }
}
